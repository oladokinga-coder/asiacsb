export function getT(locale: Locale) {
  return (key: string): string => translations[locale]?.[key] ?? key;
}

/** Для серверных компонентов: получить locale из значения cookie */
export function getLocaleFromCookie(cookieValue: string | undefined): Locale {
  return cookieValue && LOCALES.includes(cookieValue as Locale) ? (cookieValue as Locale) : "en";
}
